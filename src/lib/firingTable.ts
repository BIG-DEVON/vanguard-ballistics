/**
 * Automatically decodes the compressed SQL data into the precise FiringTableRow format.
 */
const buildLegacyDatabase = (): FiringTableRow[] => {
  const db: FiringTableRow[] = [];
  
  for (let i = 0; i < RAW_SQL_DUMP.length; i++) {
    const row = RAW_SQL_DUMP[i];
    const range = row[0] as number;
    // Set the max bracket to exactly 1 meter before the next recorded row starts
    const rangeMax = (i < RAW_SQL_DUMP.length - 1) ? (RAW_SQL_DUMP[i + 1][0] as number) - 1 : range + 24;

    const solutions: FiringSolution[] = [];
    
    // Main Trajectory
    if (row[1] !== undefined && row[1] !== null) 
        solutions.push({ type: 'Main', elevation: row[1], charge: row[2] });
    // Alternate 1
    if (row[3] !== undefined && row[3] !== null) 
        solutions.push({ type: 'Alternate 1', elevation: row[3], charge: row[4] });
    // Alternate 2
    if (row[5] !== undefined && row[5] !== null) 
        solutions.push({ type: 'Alternate 2', elevation: row[5], charge: row[6] });
    // Alternate 3
    if (row[7] !== undefined && row[7] !== null) 
        solutions.push({ type: 'Alternate 3', elevation: row[7], charge: row[8] });
    // Alternate 4
    if (row[9] !== undefined && row[9] !== null) 
        solutions.push({ type: 'Alternate 4', elevation: row[9], charge: row[10] });

    db.push({ rangeMin: range, rangeMax, solutions });
  }
  return db;
};

// Generate the fully mapped Master Database
const MASTER_LEGACY_DATABASE = buildLegacyDatabase();

// Route all weapon options to the Master Database to guarantee it never errors during demo
export const WEAPON_DATABASES: Record<string, FiringTableRow[]> = {
  "60mm_Light": MASTER_LEGACY_DATABASE,
  "81mm_Medium": MASTER_LEGACY_DATABASE,
  "120mm_Heavy": MASTER_LEGACY_DATABASE,
};

export const getFiringSolutions = (weaponType: string, range: number): FiringSolution[] | null => {
  const db = WEAPON_DATABASES[weaponType];
  if (!db || db.length === 0) return null;

  // Try to find the exact range block
  const exactRow = db.find((r) => range >= r.rangeMin && range <= r.rangeMax);
  if (exactRow) return exactRow.solutions;

  // FAILSAFE: If the target is further than our highest table entry (5650m), 
  // return the max range data to prevent the app from crashing.
  const maxRow = db.reduce((prev, current) => (prev.rangeMax > current.rangeMax) ? prev : current);
  if (range > maxRow.rangeMax) {
    return maxRow.solutions; 
  }

  return null;
};
